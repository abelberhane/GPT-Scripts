// Role-prompt: Write a beginner-friendly "Hello, World!" program in JavaScript, demonstrating best practices for function structure.

function main() {
    // Encapsulating code in a main function to demonstrate good structuring practices for beginners.
    const message = 'Hello, World!';  // Using 'const' to define the message variable, teaching the concept of variable immutability.
    console.log(message);  // Logging the message to the console, a common task in JavaScript applications.
}

// Calling the main function to execute the program, showing beginners how functions are invoked.
main();
